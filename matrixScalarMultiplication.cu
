#include <stdio.h>
#include <cuda.h>

__global__ void matrixScalarMul(float* A, float scalar, float* B, int rows, int cols) {
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    int size = rows * cols;
    if (idx < size) B[idx] = A[idx] * scalar;
}

int main() {
    int rows = 4, cols = 4;
    int size = rows * cols * sizeof(float);
    float scalar = 2.5f;
    float h_A[16], h_B[16];
    for (int i = 0; i < rows * cols; i++) h_A[i] = i + 1;

    float *d_A, *d_B;
    cudaMalloc((void**)&d_A, size);
    cudaMalloc((void**)&d_B, size);

    cudaMemcpy(d_A, h_A, size, cudaMemcpyHostToDevice);

    int threadsPerBlock = 256;
    int blocksPerGrid = (rows * cols + threadsPerBlock - 1) / threadsPerBlock;
    matrixScalarMul<<<blocksPerGrid, threadsPerBlock>>>(d_A, scalar, d_B, rows, cols);

    cudaMemcpy(h_B, d_B, size, cudaMemcpyDeviceToHost);

    for (int i = 0; i < rows * cols; i++) {
        printf("%.2f ", h_B[i]);
        if ((i + 1) % cols == 0) printf("\n");
    }

    cudaFree(d_A);
    cudaFree(d_B);
    return 0;
}
